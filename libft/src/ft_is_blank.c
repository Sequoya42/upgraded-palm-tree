#include "libft.h"

int								is_blank(char *s)
{
	int							i;

	i = 0;
	while (s[i])
	{
		if (s[i] != '\t' && s[i] != ' ' && s[i] != '\n')
			return (0);
		i++;
	}
	return (1);
}
